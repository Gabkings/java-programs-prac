    public void search(){
        String al = jTextField1.getText();
        try{
            String sql = "SELECT * FROM Account WHERE username ='"+al+"'";
             pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            if(rs.next()){
                name.setText(rs.getString(1));
                quizz.setText(rs.getString(4));
                password.setText(rs.getString(3));
            }else{
                JOptionPane.showMessageDialog(null, "Incorrect username");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    public void Retrieve(){
        String a1 = quizz.getText();
        String a2 = answer.getText();
        String sql = "SELECT * FROM Account WHERE answer='"+a2+"'";
        try{
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            if(rs.next()){
                password.setText(rs.getString(3));
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }

